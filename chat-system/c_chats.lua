bindKey("u" , "down" , "chatbox", "quickreply") -- PM Quick Reply

function playPmSound()
	local pmsound = playSound("sounds/messagesound.mp3", false)
	setSoundVolume(pmsound, 0.9)
	setWindowFlashing(true, 10)
end
addEvent("pmSoundFX", true)
addEventHandler("pmSoundFX", getRootElement(), playPmSound)

function clearChat()
	local lines = getChatboxLayout()["chat_lines"] * 2
	for i=1, lines do outputChatBox("") end
end
addCommandHandler("clearchat", clearChat)
addCommandHandler("cl", clearChat)