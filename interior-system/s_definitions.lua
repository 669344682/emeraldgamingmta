function showInteriorList(thePlayer)
	if exports.global:isPlayerHelper(thePlayer) or exports.global:isPlayerMappingTeam(thePlayer) or exports.global:isPlayerDeveloper(thePlayer) then

		-- Count how many interiors we have.
		local count = 0
		for _, int in pairs(interiors) do
			count = count + 1
		end

		outputChatBox("Loading " .. count .. " interiors..", thePlayer, 75, 230, 10)
		-- Get interiors and output them.
		for i = 1, count do
			outputChatBox("[Interior #" .. i .. "] " .. interiors[i][6], thePlayer, 75, 230, 10)
		end
	end
end
