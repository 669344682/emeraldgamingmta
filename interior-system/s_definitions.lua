function previewInterior(thePlayer, commandName, id)
	if exports.global:isPlayerLeadManager(thePlayer) or exports.global:isPlayerDeveloper(thePlayer) then
		if not tonumber(id) then
			outputChatBox("SYNTAX: /" .. commandName .. " [Interior ID]", thePlayer, 75, 230, 10)
			outputChatBox("For a list of valid interior IDs, use /intlist", thePlayer, 75, 230, 10)
		else
			-- Check how many interiors exist.
			local count = 0
			local id = tonumber(id) -- Convert id to a numerical value so it can be indexed when we fetch  data
			for _, int in pairs(interiors) do
				count = count + 1
			end

			-- If the ID given doesn't exist // Number is greater than total amount of interiors
			if (id > count) or (id == 0)  then
				outputChatBox("ERROR: Interior ID " .. id .. " does not exist!", thePlayer, 255, 0, 0)
				return false
			end

			-- Get interior data
			local newint = interiors[id][1]
			local newx = interiors[id][2]
			local newy = interiors[id][3]
			local newz = interiors[id][4]
			local rotation = interiors[id][5]
			local intName = interiors[id][6]
			local randomDim = math.random(50000, 60000)

			setElementInterior(thePlayer, newint)
			setElementPosition(thePlayer, newx, newy, newz)
			setElementRotation(thePlayer, 0, 0, rotation)
			setElementDimension(thePlayer, randomDim)

			outputChatBox("You have teleported inside interior " .. id .. ".", thePlayer, 75, 230, 10)
			outputChatBox(" ", thePlayer, 0, 0, 0)
			outputChatBox("This interior Name: " .. intName, thePlayer, 75, 230, 10)
			outputChatBox("MTA Interior ID: " .. newint, thePlayer, 75, 230, 10)
			outputChatBox("X: " .. newx, thePlayer, 75, 230, 10)
			outputChatBox("Y: " .. newy, thePlayer, 75, 230, 10)
			outputChatBox("Z: " .. newz, thePlayer, 75, 230, 10)
			outputChatBox("Rotation: " .. rotation, thePlayer, 75, 230, 10)
			outputChatBox("Your dimension has been temporarily set to " .. randomDim .. ".", thePlayer, 75, 230, 10)
		end
	end
end
addCommandHandler("previewint", previewInterior)