-- Takes the given faction ID and returns the faction name and abbreviation.
function getFactionName(factionID)
	if not tonumber(factionID) then outputDebug("@getFactionName: factionID not provided or is not a number value.") return false end

	local allFactions = getElementsByType("team")
	for i, faction in ipairs(allFactions) do
		if (getElementData(faction, "faction:id") == tonumber(factionID)) then
			local factionAbbr = getElementData(faction, "faction:abbreviation")
			return getTeamName(faction), factionAbbr
		end
	end
	return "Unknown", "?"
end